class Head:
    pass

class Brain(Head):
    def organ_name(self):
        return "Мозок"

class Eyes(Head):
    def organ_name(self):
        return "Очі"

class Ears(Head):
    def organ_name(self):
        return "Вуха"

class Nose(Head):
    def organ_name(self):
        return "Ніс"

class Mouth(Head):
    def organ_name(self):
        return "Рот"
